    <p class="lead">The first television channel in Norway, NRK1 was started
        officially in 1960 (then under the name NRK), but had regular programs since 1954.
        A second television channel, TV 2 was started in 1992. NRK started a
        second television channel, NRK2 in 1996. NRK1 goes out to 99,8% of all households,
        while TV2 has a 92% coverage and NRK2 and NRK3/NRK Super has 80% coverage.
        There are today 24 local television channels in the country,
        all terrestrially transmitted. The first local channel,
        TVBudstikka was started in 1986. Many of them cooperate with TVNorge,
        so that TVNorge use their transmitters when they are not broadcasting any programs.</p>
    <h2 class="h2">Cable and satellite television</h2>
    <p class="lead">When the television and radio monopoly of NRK was lifted in 1982,
        cable television networks that carried foreign TV channels started to
