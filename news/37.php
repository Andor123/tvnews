    <h1 class="h1">Television in Portugal</h1>
    <p class="lead">Source: <em>Wikipedia</em></p>
    <p class="lead">Television in Portugal was introduced in 1956
        (test broadcasts) by Radiotelevisão Portuguesa
        (now named Rádio e Televisão de Portugal),
        which held the nationwide television monopoly until late 1992.
        Regular broadcasting was introduced on March 7, 1957.
        Colour transmissions were introduced on March 10, 1980.</p>
    <p class="lead">Digital terrestrial television (DTT)
        was introduced at a very late stage when compared
        to other countries in Europe and with limited channels,
        and according to the European Audiovisual Observatory
        it occupies the last place in 34 European countries
        with the weakest offer on digital terrestrial television.
        In such a way that most Portuguese are subscribers of cable
        (HFC) or IPTV (DSL or FTTH) platforms, in percentages higher
        than in the rest of Europe and these platforms are well developed
        with a large number of channels. During the transition from analog
        to DTT, subscription-based television services experiences a
        10% increase and reached 72.5% of homes in 2012.
        Regional and local television is also limited. Portuguese
        television is regulated by the
        Entidade Reguladora para a Comunicação Social (ERC).
        In 2016, TVI was the ratings leader with 21.5%
        share while SIC, RTP1 and RTP2 had 17.6%, 13.7% and 2.0%,
        respectively.</p>
    <h2 class="h2">Terrestrial</h2>
    <p class="lead">Analog broadcasts in Portugal were discontinued
        on April 26, 2012. There are eight free-to-air channels on
        Portuguese terrestrial TV: 6 are owned by the public service
        broadcaster RTP (with 2 being regional channels that broadcast
        FTA only in the Madeira and Azores Autonomous Regions),
        2 are from private broadcasters (SIC and TVI) and 1 is owned by
        the Aseembly of the Republic and broadcasts parliamentary
        sessions (like BBC Parliament).</p>
    <h2 class="h2">IPTV</h2>
    <p class="lead">Optimus Clix has launched in 2006 a service called
        SmarTV (rebranded as Optimus Clix TV), provided on Amino
        and Motorola STBs, with VoD provided by Kasenna MediaBase
        video servers. PT Comunicações (Portugal Telecom) has also
        launched one called MEO, providing that the spin-off of
        subsidiary PT Multimédia was concluded. Vodafone also
        launched an IPTV service called Vodafone Casa TV.</p>
