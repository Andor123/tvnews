    <h1 class="h1">Television in Romania</h1>
    <p class="lead">Source: <em>Wikipedia</em></p>
    <p class="lead">Television in Romania was introduced in August 1955.
        State television started to broadcast on December 31, 1956.
        The second television channel followed in 1968,
        but between 1985 and 1990, there was only one Romanian
        channel before the return of the second channel.
        Private broadcasters arrived in the early 1990s,
        with Antena 1 being the first private television station in Romania.</p>
    <p class="lead">According to an article on the Romanian newspaper Adevărul,
        the top TV broadcasters were (based on the average prime-time
        number of viewers in Oct-Nov 2007): Pro TV (with 615.000 viewers),
        TVR1 (451.000), Antena 1 (305.000), OTV (272.000),
        Acasă TV (247.000, now Pro 2), Prima TV (205.000),
        Realitatea TV (102.000) and Antena 3 (73.000).</p>
    <p class="lead">In early 2015, it was officially announced that the
        top TV broadcasters are: Pro TV, Antena 1, Antena 3, Kanal D, Romania TV,
        Realitatea TV, TVR 1, Acasă TV (currently Pro 2), Prima TV.</p>
    <h2 class="h2">Television market</h2>
    <p class="lead">Romanian television is dominated by a
        small number of corporations, owning multiple TV
        channels as well as radio stations, newspapers and
        media agencies. Their television business is structured
        around a flagship channel and a number of smaller specialized,
        niche channels. The biggest corporations of this kind are:</p>
    <ul class="list-group">
        <li class="list-group-item">Intact Media Group (with Antena 1-Antena 5)</li>
        <li class="list-group-item">Central European Media Enterprises (with Pro TV,
            Pro 2, Pro Gold, Pro Cinema, Pro TV Internațional and Pro X)</li>
        <li class="list-group-item">Realitatea-Catavencu (with Realitatea TV,
            Romantica and The Money Channel)</li>
        <li class="list-group-item">Centrul Naţional Media (with Naţional TV,
            N24 and Favorit TV)</li>
        <li class="list-group-item">SBS Broadcasting Group (with Prima TV and Kiss TV)</li>
    </ul>
    <p class="lead">Additionally, there is a public television service operated by Televiziunea Română
        with four channels (TVR 1, TVR 2, TVR Cultural and TVR i) and there are many
        localized or franchised international channels (such as HBO, MTV, Cinemax, AXN,
        Cartoon Network). Furthermore, there are a few independent and local broadcasters.</p>
    <p class="lead">Two private stations, Pro TV and Antena 1, are market leaders, sharing
        about 32% of the market, with public television in the third place.
        A feature of Romanian Television after 2000 was the boom of specialized channels
        - such as soap opera and telenovela channels (Pro 2, Romantica, Happy Channel),
        sport channels (such as Pro X and Telesport), talk channels (Antena Stars)
        news channels (Realitatea TV, Antena 3 and N24), different movie genres or
        documentary types, and even specializing on different musical styles
        (UTV Romania and MTV being geared toward club, dance and hip-hop music,
        whereas Favorit TV and Etno TV towards folklore, and Taraf TV towards manele).
        Teleshopping channels, lifestyle and weather channels, and special channels dedicated
        for display in public areas (such as waiting areas, airports, train stations, banks, supermarkets)
        additionally exist.</p>
    <p class="lead">Television broadcasts and cable television, frequency allocations,
        content monitoring and license allocation are done by the
        National Audiovisual Council (Consiliul Naţional al Audiovizualului (CNA)).</p>
