<section class="container-fluid text-center" id="section">
    <h1 class="h1">Television in Finland</h1>
    <p class="lead">Source: <em>Wikipedia</em></p>
    <p class="lead">Television was introduced in Finland in 1955.
        Color television started in 1969. Prior to 1986,
        Yle monopolized the Finnish television. All terrestrial analogue
        stations stopped broadcasting on 1 September 2007 after introducing
        digital television; cable providers were allowed to continue analog
        broadcasting in their networks until 1 March 2008.</p>
    <p class="lead">Typically, foreign-language content is subtitled,
        retaining the original language soundtrack.
        This includes interview responses in news or magazine programmes
        not given in the main language of that programme.
        Foreign programming intended for children is, however,
        usually dubbed into one of the national languages.
        Regardless of the intended audience or original language,
        many shows receive a Finnish and/or Swedish title which
        is used in programme schedules.</p>
    <h2 class="h2">Digital terrestrial</h2>
    <p class="lead">Digital terrestrial television was launched on 21 August 2001.
        The analogue networks continued its broadcasts alongside the digital ones
        until 1 September 2007, when they were shut down nationwide.</p>
    <p class="lead">Before the analogue switchoff, the terrestrial network had three multiplexes:
        MUX A, MUX B and MUX C. MUX A contained the channels of the public broadcaster
        Yleisradio and MUX B was shared between the two commercial broadcasters:
        MTV3 and Nelonen. MUX C contained channels of various other broadcasters.
        After the analogue closedown, a fourth multiplex named MUX E was launched.</p>
    <p class="lead">In addition the free-to-air broadcasts,
        two companies are providing encryption cards for pay television:
        Canal Digital and PlusTV. Canal Digital was the first to launch,
        originally only offering four Canal+ channels (the Disney Channel
        was added later on). PlusTV was launched in November 2006,
        originally only broadcasting MTV3 Max and Subtv Juniori
        (later on adding Subtv Leffa and Urheilu+kanava). Both packages
        got more channels with the launch of MUX E in September 2007:
        SVT Europa and MTV3 Fakta was added to PlusTV and KinoTV was added
        to Canal Digital, while Discovery Channel, Eurosport, MTV Finland
        and Nickelodeon were added to both packages.</p>
    <p class="lead">September 2007 also saw the launch of the SveaTV
        package in Ostrobothnia which broadcasts channels from Sweden.</p>
    <p class="lead">The digital channel YLE Extra was closed on 31 December 2007
        and was replaced by YLE TV1+, a simulcast of TV1 with subtitles
        included in the video stream. TV1+ was closed on
        4 August 2008 due to its low viewing share.</p>
    <p class="lead">Finland has started DVB-T2 switchover
        that will be finished on 31.3.2020.</p>
    <h2 class="h2">Cable</h2>
    <p class="lead">Analogue cable television were switched off in Finland on
        1 March 2008, but digital cable television is widespread all
        over the country and its infrastructure used for cable internet services.</p>
    <p class="lead">The major cable operators are DNA, Welho and TTV,
        operating in Turku, Helsinki and Tampere areas. All pay television
        uses digital broadcasts, DVB-C set-top boxes have been available since 2001.</p>
