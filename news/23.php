    <h2 class="h2">Satellite television</h2>
    <p class="lead">The first Soviet communication satellite,
        called Molniya, was launched in 1965.
        By November, 1967 the national system of satellite television,
        called Orbita was deployed. The system consisted of 3 highly
        elliptical Molniya satellites, with Moscow-based ground
        uplink facilities and about 20 downlink stations,
        located in cities and towns of remote regions of
        Siberia and the Far East. Each station had a 12-meter receiving
        parabolic antenna and transmitters for re-broadcasting
        TV signals to local householders.</p>
    <p class="lead">However, a large part of the Soviet central regions
        were still not covered by transponders of Molniya satellites.
        By 1976 Soviet engineers developed a relatively simple and inexpensive system
        of satellite television (especially for Central and Northern Siberia).
        It included geostationary satellites called Ekran equipped with powerful
        300-watt UHF transponders, a broadcasting uplink station and various
        simple receiving stations located in various towns and villages of Siberia.
        The typical receiving station, also called Ekran, included a home-use
        analog satellite receiver equipped with a simple Yagi-Uda antenna.
        Later, Ekran satellites were replaced by more advanced
        Ekran-M series satellites.</p>
    <p class="lead">In 1979 Soviet engineers developed the Moskva
        (or Moscow) system of broadcasting and delivering of
        TV signals via satellites. New types of geostationary
        communication satellites, called Gorizont, were launched.
        They were equipped with powerful onboard transponders, so the
        size of the receiving station's parabolic antennas were reduced to
        4 and 2.5 meters (in comparison to the early 12- meter dishes of
        the standard orbital downlink stations).</p>
    <p class="lead">By 1989 an improved version of the Moskva system,
        called Moskva Global'naya, (or Moscow Global) was introduced.
        The system included a few geostationary Gorizont and Express
        type communication satellites. TV signals from Moscow Global’s
        satellites could be received in any country on the planet except
        Canada and the Northwest USA.</p>
    <p class="lead">Modern Russian satellite broadcasting services are based on
        powerful geostationary satellite buses such as Gals (satellite),
        Ekspress, USP and Eutelsat which provide a large quantity of free-to-air
        television channels to millions of householders.
        Pay-TV is growing in popularity amongst Russian TV viewers.
        The NTV Russia news company, owned by Gazprom, broadcasts
        the NTV Plus package to 560,000 households,
        reaching over 1.5 million viewers.</p>
    <p class="lead">Six out of these seven satellites are new vehicles.
        Four belong to the "Express-AM" family (sent into orbit in 2003-2005),
        and two to the "Express-A" family (sent into orbit in 2000-2002).
        SESC also uses the centre for TV/Radio signal compression standard
        along with the formation of data transport flows as per the MPEG-2/DVB standard,
        which ensures the formation of standardized signal packages
        from federal TV/radio channels.</p>
    <p class="lead">By May 2013, of the 53 million TV homes in Russia,
        24% were equipped for Direct-to-Home satellite reception,
        making satellite the country’s leading platform for digital television.
        The number of satellite homes across Russia continues to grow,
        increasing by 25% between 2011 and 2013 from 8 million to 12.6 million.
        10% of these homes receive signals from more than one satellite position,
        taking the total number of antennas to 13.8 million.</p>
    <h2 class="h2">Cable television</h2>
    <p class="lead">Cable television was introduced in the 2000s,
        and grew significantly in the early 2010s.
        Cable operators began upgrading their networks to DVB-C and adding
        new services such as video on demand, catch-up-TV and others.
        In 2012, cable television accounted for more than half of
        all pay-TV subscribers (58%).</p>
