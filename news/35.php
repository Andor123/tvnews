    <h1 class="h1">Television in Spain</h1>
    <p class="lead">Source: <em>Wikipedia</em></p>
    <p class="lead">Television in Spain was launched in October 1956,
        when the state broadcaster Televisión Española (TVE) started regular broadcasts.
        The first private channels started in 1990. Colour transmissions started
        in 1974 after two years of test transmissions, with all programming
        transmitted in color from 1977, and colour commercials, which started in 1978.
        Currently, television is one of the leading mass media of the country,
        and by 2008 was in 99.7% of households in Spain according to INE statistics.</p>
    <p class="lead">Until recently terrestrial television was considered
        an essential public service. Broadcasting is managed both directly by
        the State and indirectly, through controlled concessions to private firms.
        The Audiovisual Law of 2010 changed this by defining radio
        and television as commercial services that individuals pay for,
        fostering liberalization within some constraints.</p>
    <h2 class="h2">Analogue terrestrial television</h2>
    <p class="lead">Analogue terrestrial television in Spain began on 28 October 1956.
        Televisión Española (now La 1) was the very first regular television channel,
        and operated alone until 1966, when a second channel (now La 2) was launched.
        La 1 and La 2 were the only authorized television channels in Spain until 1982,
        when Euskal Telebista was launched in the Basque Country and, in the following year,
        TV3 in Catalonia, thus ending TVE's monopoly. More channels were launched in
        the 1980s, each broadcasting to their respective autonomous communities:
        Canal 9, Canal Sur, Telemadrid, and Televisión de Galicia (TVG), prior
        to the full liberalization of television with the law of 1989 which
        permitted the establishment of private commercial channels.</p>
    <p class="lead">Antena 3, Telecinco and Canal+ were launched in 1990,
        in January, March and September respectively, finishing the public
        television monopoly in Spain. Through the 1990s and 2000s (decade),
        more autonomic channels (most of them public, but some of them private)
        were launched, and all of them created FORTA, a union of public autonomic channels.
        Many local channels were also launched, some of them created the Localia Network.
        During the 1990s, dozens of local channels started broadcasting without a license.
        The government declared that channels that proved to be operating for a long
        time could go on working, but blocked new unlicensed channels.</p>
    <p class="lead">In the 2000s (decade), the analog national and autonomic
        channels started simulcast on Digital Terrestrial Television. In 2005,
        Canal+ stopped its analog service to move to Digital Plus and was
        substituted by Cuatro. Some weeks later, the last analog national private channel,
        La Sexta, started testing broadcasts, to begin its regular analog
        schedule in 2006. In 2009, the analog service started its closure in
        a process that lasted one year. On 3 April 2010 the analog service was
        officially discontinued but there are still some small local analog
        channels operating, most of them the unlicensed analog channels.</p>
    <h2 class="h2">Digital terrestrial television</h2>
    <p class="lead">The development of digital terrestrial television was
        very similar to the failure of ITV Digital in the United Kingdom.
        Digital terrestrial television was introduced in the country by the
        pay per view platform Quiero Television. In May 2002, statewide operators
        were required to start broadcasting in DVB-T. Yet, Quiero TV ceased transmissions
        in 2002 after a commercial failure. Unlike the UK, the three and half multiplexes
        left by the platform were not reassigned to other operators,
        and so 5 channels were squashed into a single multiplex.</p>
    <p class="lead">On 30 November 2005 Digital Terrestrial Television was
        relaunched as a free service with 20 channels and 14 radio stations,
        along with 23 regional- and local-language channels in their respective areas.
        Currently about 95% of the population can receive DTT. Each multiplex has
        a minimum of 4 SD channels each or one HD channel. Televisió de Catalunya
        - TVC and Aragón Televisión are using spare bandwidth in their own digital
        multiplex to broadcast test HD streams.</p>
