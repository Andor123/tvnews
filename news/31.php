    <h1 class="h1">Television in Italy</h1>
    <p class="lead">Source: <em>Wikipedia</em></p>
    <p class="lead">Television in Italy was introduced in 1939, when the
        first experimental broadcasts began. However, this lasted for a very short time:
        when fascist Italy entered World War II in 1940 all transmissions were interrupted,
        and were resumed in earnest only nine years after
        the end of the conflict, on January 3, 1954.</p>
    <p class="lead">There are two main national television organisations responsible for most viewing:
        state-owned RAI, accounting for 37% of the total viewing figures in May 2014,
        and Mediaset, a commercial network which holds about 33%.
        The third largest player, the Italian branch of Discovery Communications,
        had a viewing share of 5.8%. Apart from these three free to air companies,
        News Corporation's satellite pay TV platform Sky Italia is increasing in viewing and shares.</p>
    <p class="lead">According to the BBC, the Italian television industry is
        widely considered both inside and outside the country to be overtly politicized.
        Unlike the BBC which is controlled by an independent trust,
        the public broadcaster RAI is under direct control of the parliament.
        According to a December 2008 poll, only 24% of Italians trusted
        television news programmes, compared unfavourably to the British rate of 38%,
        making Italy one of only three examined countries where online
        sources are considered more reliable than television ones for information.</p>
    <h2 class="h2">Digital terrestrial television</h2>
    <p class="lead">Digital terrestrial television technology is expanding rapidly
        and now every major network in Italy,—including RAI, Mediaset and
        Cairo Communication—transmits in DVB-T format, while continuing
        analog broadcast until the end of the transition, originally set
        by law to December 31, 2006 but later pushed back to the end of 2012.</p>
    <p class="lead">The Berlusconi II Cabinet started promoting the digital
        format in December 2003 by granting a public financial contribution
        for the purchase of a MHP digital television decoder.
        Starting from January 2005 Mediaset and Telecom Italia Media started
        offering pay TV services through a prepaid smartcard, including football games,
        movies and TV shows. On February 2006, during the
        2006 Winter Olympics held in Turin, RAI experimentally broadcast a number of
        sport events using a 1080i signal and H264 coding.
        The HD signal has been transmitted over the Turin area,
        using DVB-T hierarchical modulation, and only specially crafted decoders
        have been able to receive this signal: they were placed
        in strategical points in the town.</p>
    <p class="lead">During the UEFA Euro 2008 and the 2008 Summer Olympics,
        RAI started experimental High Definition broadcasting on Rai Test HD,
        available only in Turin, Milan, Rome, Sardinia and Aosta Valley,
        continuing with the 2008 UCI Road World Championships and a few matches of
        the UEFA Champions League. In July 2008 the European Commission's directorate
        for competition expressed concerns on whether the actions taken by the current
        Italian government would be able to alter the current status of duopoly in the
        broadcasting market held by RAI and Mediaset.[5] Beginning October 31, 2008,
        in the first region of Italy planned to interrupt analog transmission, Sardinia,
        television networks broadcast multiplexes only in digital format. Licence fee payers
        from the region were entitled to a 50 euros discount off the price of a digital
        television decoder or a new, digital-compatible TV set.</p>
    <h2 class="h2">Satellite television</h2>
    <p class="lead">Italy has had digital satellite broadcasts since the 1990s,
        with the launch of Stream TV and TELE+. In 2003 these merged into Sky Italia,
        today this pay TV platform is broadcasting from Hotbird satellites.
        HDTV regular services started in June 2006 under the name Sky HD,
        with the broadcasting of the 2006 FIFA World Cup in High Definition.
        Additional movie and sport channels are planned for the service. Tivù Sat,
        a Free Satellite Service similar to the UK version Freesat,
        was launched in June 2009, ensuring access to national television channels
        from digital terrestrial television networks. Shareholders include Mediaset,
        Telecom Italia Media and the State-owned company Rai.</p>
