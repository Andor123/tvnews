    <h1 class="h1">Television in Greece</h1>
    <p class="lead">Source: <em>Wikipedia</em></p>
    <p class="lead">Television broadcasting in Greece began in 1966 and this
        was preceded in 1951 by statute 1663 permitting television broadcasting.</p>
    <h2 class="h2">Digital Terrestrial Television</h2>
    <p class="lead">In 2005, Alpha TV briefly ran experimental broadcasts in DVB-T locally in Athens,
        while in Heraklion, Crete, a regional network, Kriti TV,
        in conjunction with the University of Crete, also started digital operation.</p>
    <p class="lead">In January 2006, the State broadcaster ERT launched free-to-air Digital terrestrial
        television (DVB-T) with three "pilot" channels called Prisma+, Cine+ and Sport+,
        collectively branded as ERT Digital. The first channel, Prisma+, was targeted at
        disabled persons, while Cine+ broadcasts movies, and Sport+ broadcasts a sports program.
        A fourth channel, the Cypriot national channel's satellite program RIK sat,
        was also retransmitted on digital.</p>
    <p class="lead">From September 2009 onwards, the seven major television stations in Greece broadcast
        in DVB-T MPEG-4, via a company set up by them, Digea. Because of the concentration
        of the population in a few cities, about 75% of the Greeks are now able to view DTT,
        although only a small proportion of the geographical area of Greece is covered. Areas
        covered (as at August 2011) include Attica, Thessalonica, Larissa, Alexandropolis,
        the Corinthian Gulf, and Rhodes Town.</p>
    <p class="lead">On February 6, 2015, the transition to digital TV in Greece was completed.
        A digital-ready TV or set-top box is required to view digital channels.</p>
    <h2 class="h2">Cable TV</h2>
    <p class="lead">First CABLE TV operator in Greece, is located at Kalamaria (Thessaloniki).
        Hellenic Cable Networks (www.hcn.gr) among cable internet services offers DVB-C and
        analog TV services without the need of an external STB.</p>
