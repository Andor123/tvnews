    <h1 class="h1">Television in Ukraine</h1>
    <p class="lead">Source: <em>Wikipedia</em></p>
    <p class="lead">Television has a long history in Ukraine,
        with regular television broadcasting started during the
        former USSR years in 1951.
        However the first ever TV broadcast took place on the
        1st February 1939 in Kyiv. Since then TV broadcasting has expanded,
        particularly after the fall of Communism in 1989,
        and now there are many different channels and groups
        in the Ukrainian TV market.</p>
    <h2 class="h2">Digital television</h2>
    <p class="lead">In 2007 and 2008, experimental DVB-T broadcasts of
        few channels started in Kyiv and Odesa.
        Those turned out to be successful.
        Yet the DTT National Program is not approved by the government,
        thus the process is stuck. Because there are two versions of the
        program submitted: from the Ministry of Transport and Communications as
        well as from the State Committee of Television and Radio,
        there is no particular progress in 2008.</p>
    <p class="lead">On 26 November 2008, the National Program of the Ministry
        was approved, but the final version and the public announcement of
        this fact is still on hold. Current version of the program does not take
        into notice any kind of Government financing, and the budget
        is to be private only, which will highly affect the
        TV industry and commercial broadcasters.</p>
    <p class="lead">Besides there are 3rd parties, such as Television Industry Committee
        and National Association of Broadcasters which represent the communities
        of National and regional broadcasters respectively.
        Both organizations help the switchover not to affect the business
        of over 20 National and over 150 regional broadcasters.</p>
    <p class="lead">The International Forum 'Digital Broadcasting in Ukraine'
        is the annual event that takes place in Kyiv, Ukraine.
        Its mission is to gather the most of international consultants and
        Ukrainian specialists to solve industry's problems in the DTT field.
        In 2008 the 2nd International Forum took place in Kyiv also. BBC,
        Deloitte and the Ministry of Communications of Finland representatives
        share the vision of possible plan of DTT implementation in Ukraine,
        delivering the best experiences from UK, Finland, France and US.
        Still none was taken into notice yet. It is now confirmed that Ukraine's national
        terrestrial TV network, which is scheduled to be launched in September 2011,
        will use the DVB-T2 standard for all four nationwide FTA multiplexes,
        for both SD and HD broadcasts. Before settling for DVB-T2, Ukraine was
        testing both DVB-T/MPEG-2 and DVB-T/MPEG-4 options, and some experimental
        transmitters operating in those standards are still live.</p>
    <h2 class="h2">Other technologies</h2>
    <p class="lead">Commercial MMDS digital TV services work
        in Kyiv and some other cities.</p>
    <p class="lead">DVB-C services delivering premium channels
        (in addition to standard analogue channels) launched in
        cable networks of Kiev, Odesa, Kremenchuk, Poltava, Donetsk
        and some other cities.</p>
