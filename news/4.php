    <h1 class="h1">Television in Poland</h1>
    <p class="lead">Source: <em>Wikipedia</em></p>
    <p class="lead">Television was introduced in Poland in 1937.
        It was state owned, and was interrupted by the Second World War in 1939.
        Television returned to Poland in 1952 and
        for several decades was controlled by the communist government.
        Colour television was introduced in Poland in 1971.
        Private television stations in Poland appeared around the time
        of the fall of communism, with PTV Echo
        (once a member of the local channels of Polonia 1)
        becoming the first private station in Poland (and in the former Eastern Bloc).</p>
    <h2 class="h2">Terrestrial</h2>
    <p class="lead">Terrestrial television in Poland is broadcast using a digital DVB-T system.
        First test DVB-T emission was carried in Warsaw at 9 November 2001.
        In April 2004, first DVB-T transmitter near Rzeszów started operation,
        and local TVP division started to market set-top boxes allowing to receive it.
        As of July 2016, there are about 250 DVB-T transmitters operating in Poland,
        broadcasting up to three multiplexes (except local stations),
        all using MPEG-4 AVC compression. Three channels, TVP1, TVP2 and TVP Info
        are available in HD across all of Poland, while several others
        (Polsat, TVN, TVN7 and TTV) broadcast in HD in some local multiplexes.
        Analog terrestrial transmissions were terminated in 2013.</p>
    <p class="lead">First efforts to introduce DVB-T in Poland was made in
        1997 in Gdańsk on initiative of TVP (Polish public television broadcaster).</p>
    <h2 class="h2">Satellite</h2>
    <p class="lead">In 1998, two DVB-S providers, Cyfra+ and Wizja TV started operation. In 2001, Wizja TV was merged into Cyfra+.
        1999 saw the introduction of another platform, Polsat 2 Cyfrowy (later renamed to Polsat Cyfrowy and finally Cyfrowy Polsat),
        owned by Polsat, a major Polish commercial TV network.
        These platforms become very popular, and today, there are no Polish channels available on
        analogue satellite (the last one, TVN, was switched off in 2008). However, most of the
        satellite broadcasts in Poland are in SDTV resolution.
        On 12 October 2006, ITI launched the n platform with few HD channels available,
        as well as - also new in Poland - video on demand functionality. Later,
        in December 2006, Cyfra+ also started HD broadcasts.
        Cyfrowy Polsat started HD broadcasts in second half of 2007.
        In March 2013, as part of deeper cooperation between Canal+ Group and ITI, Cyfra+ and N were merged into NC+.</p>
