    <h1 class="h1">Television in the United Kingdom</h1>
    <p class="lead">Source: <em>Wikipedia</em></p>
    <p class="lead">Television in the United Kingdom started in 1936
        as a public service which was free of advertising.
        Currently, the United Kingdom has a collection of
        free-to-air, free-to-view and subscription services
        over a variety of distribution media, through which
        there are over 480 channels for consumers as well
        as on-demand content. There are six main channel
        owners who are responsible for most viewing.
        There are 27,000 hours of domestic content produced
        a year at a cost of £2.6 billion. Since 24 October 2012,
        all television broadcasts in the United Kingdom are in a
        digital format, following the end of analogue transmissions
        in Northern Ireland. Digital content is delivered via
        terrestrial, satellite and cable as well as over IP.</p>
    <h2 class="h2">Broadcast television providers</h2>
    <p class="lead">Free-to-air, free-to-view and subscription
        providers are available, with differences in the number
        of channels, capabilities such as the programme guide (EPG),
        video on demand (VOD), high-definition (HD), interactive
        television via the red button, and coverage across the UK.
        The UK's five most watched channels, BBC One, BBC Two, ITV,
        Channel 4 and Channel 5, are available from all providers.</p>
    <p class="lead">Broadcast television is distributed as radio waves
        via terrestrial or satellite transmissions, or as electrical or
        light signals through ground-based cables. In the UK, these use
        the Digital Video Broadcasting standard. Most TVs sold in the
        UK come with a DVB-T (terrestrial) tuner for Freeview – a rare
        thing in Europe. Set-top boxes are generally used to receive channels
        from other providers. Most services have also integrated their
        broadcast TV services with additional video streams distributed
        via the Internet, or through their own Internet Protocol network.</p>
    <h2 class="h2">Internet video services</h2>
    <p class="lead">TV via the Internet can be streamed or downloaded,
        and consist of amateur or professionally produced content.
        In the UK, most broadcasters provide catch-up TV services which
        allow viewing of TV for a window after it was broadcast.
        Online video can be viewed via mobile devices, computers,
        TVs equipped with a built in Internet connection, or TVs
        connected to an external set-top-box, streaming stick or games
        console. Most of the broadcast TV providers have integrated their
        set-top-boxes with Internet video to provide a hybrid broadcast
        and online service.</p>
