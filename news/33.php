<section class="container-fluid text-center" id="section">
    <h1 class="h1">Television in France</h1>
    <p class="lead">Source: <em>Wikipedia</em></p>
    <p class="lead">Television in France was introduced in 1931,
        when the first experimental broadcasts began.
        Colour television was introduced in October 1967 on La Deuxième Chaîne.</p>
    <h2 class="h2">Digital</h2>
    <p class="lead">Télévision Numérique Terrestre is the national digital terrestrial
        television service for France. It formally arrived on 31 March 2005 after a
        short testing period. Like Freeview in the UK, it provides many new channels,
        as well as the current terrestrial television stations.
        Like the rest of the Europe, France uses the DVB-T transmission technology.</p>
    <p class="lead">By 2012, the digital terrestrial television services
        were expected to cover at least 95% of the French metropolitan population.
        Five high-definition (HD) channels (four free-to-air and one subscription)
        were launched in October 2008 using also the H.264 format.
        In September 2005, pay television channels were launched
        that use the MPEG-4 format, unlike most of Europe, which uses MPEG-2.</p>
    <p class="lead">Pay-per-view terrestrial channels use H.264.
        TNT is the first service to implement Dolby Digital Plus
        as an audio codec on its high-definition channels.
        Viewers must buy a TV set (or set-top box)
        that supports both MPEG-4 H.264 and DD+ to enjoy HD channels.</p>
    <p class="lead">Analog broadcasts were switched off on 30 November 2011
        on all platforms, whether it is terrestrial, satellite or cable.
        Overseas departments and territories (such as French Guiana and Martinique)
        also terminated all analog broadcasts on the same day.</p>
    <h2 class="h2">Other technologies</h2>
    <p class="lead">Most internet service providers in France now
        offer digital television (IPTV) packages through triple-play
        set-top box. However, some subscribers have too much attenuation
        distortion on their lines to benefit from the service,
        and France does not yet have 100% DSL coverage.</p>
    <p class="lead">French cable providers Noos SA and UPC France SA
        and Numericable merged to become the largest cable operator in France.
        They provide Cable Television (using multiple brands)
        through their set top boxes.</p>
    <p class="lead">Digital satellite television has existed in France since 1997.
        HDTV transmissions began in April 2006,
        when CanalSat launched its first HD channel (Canal+ HD).
        Télévision Par Satellite and CanalSat have merged in 2007,
        leaving Nouveau Canalsat and Bis Télévisions as the
        two main competitors for the satellite television market in the country.</p>
