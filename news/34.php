    <h1 class="h1">Television in the Netherlands</h1>
    <p class="lead">Source: <em>Wikipedia</em></p>
    <p class="lead">Television in the Netherlands was introduced in 1951.
        In the Netherlands, the television market is divided between a
        number of commercial networks, such as RTL Nederland,
        and a system of public broadcasters sharing three channels,
        NPO 1, NPO 2, and NPO 3. Imported programmes (except those for children),
        as well as news interviews with responses in a foreign language,
        are almost always shown in their original language, with subtitles.</p>
    <h2 class="h2">High-definition</h2>
    <p class="lead">In the Netherlands customers can receive
        high-definition television channels by cable or satellite.
        There is no terrestrial HD service available nor planned.
        The first trials with high-definition television in the Netherlands
        began in 2006 with the broadcast of the 2006 World Cup in HD.
        After the trial the larger cable companies continued a HD
        service with a small number of channels as National Geographic Channel HD,
        Discovery HD Showcase, History HD, Film1 HD and Sport1 HD.
        But because no Dutch network had made the move to HD,
        already broadcast in widescreen and the quality of
        the standard-definition PAL signal was good enough for most people,
        demand was low.</p>
    <p class="lead">Since the 2006 trials none of the main
        Dutch networks made the move to HD. This changed in the summer of 2008
        when from June 1, 2008, until August 24, 2008,
        the Netherlands Public Broadcasting (NPO) organisations made their primary channel,
        NPO 1 temporary available in HD. This made it possible to broadcast Euro 2008,
        the 2008 Tour de France, and the 2008 Summer Olympics in HD and
        additionally allowed them to test their systems before the scheduled
        launch of their permanent HD service in early 2009. The NPO planned to
        launch their permanent HD service with HD versions of their three channels
        NPO 1, NPO 2, and NPO 3. Most of the programming in the early stages will
        consists of upscaled material from their parent channels as in time more
        programs will become available in HD. Technicolor Netherlands,
        the company responsible for the technical realisation of
        the broadcasts of the NPOs television and radio channels,
        began the summer 2008 test broadcast of NPO 1 HD in 720p/50
        as the European Broadcasting Union (EBU) recommends.
        During the test period an additional 1080i/25 version of the channel
        was made available to the cable companies because of
        quality complaints from viewers. No information has been made available
        what the format of the permanent HD service from the NPO will be.</p>
    <p class="lead">The commercial broadcasting organization in the Netherlands
        the SBS Broadcasting Group (NET 5, SBS 6, Veronica) and the
        channels of RTL Nederland are currently available in HD via cable and satellite.</p>
