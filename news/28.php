<section class="container-fluid text-center" id="section">
    <h1 class="h1">Television in Denmark</h1>
    <p class="lead">Source: <em>Wikipedia</em></p>
    <p class="lead">Television in Denmark was established in the 1950s and
        was run by a monopoly with only one channel available until the 1980s.</p>
    <h2 class="h2">Digital terrestrial television</h2>
    <p class="lead">Digital terrestrial television in Denmark was technically launched
        in March 2006 after some years of public trials. The official launch was at
        midnight on November 1, 2009, where the analogue broadcasts shut down nationwide</p>
    <p class="lead">As of September 2013, six national multiplexes are available.
        MUX 1 & 2 are owned by DIGI-TV I/S (joint-venture between DR and TV 2)
        but are operated by Teracom A/S. They broadcast free-to-air channels only.
        MUX 3 - 6 are owned and operated by Boxer broadcasting encrypted pay-TV only.</p>
    <p class="lead">On July 1, 2011 the Danish Ministry of Culture announced that MUX8 were
        to be used for digital TV. A gatekeeper as well as a launch date for MUX8
        has yet to be announced.</p>
    <h2 class="h2">National Launch</h2>
    <p class="lead">The technical launch was made on March 31, 2006
        after some months of national trials with one multiplex covering the entire country.
        It offered the national analogue channels DR1 and TV 2, as well as DR2,
        a stream for signed simulcasts of news programmes on the three
        other channels and the MHP services DR extra and TV 2 Extra.
        February 19, 2008, DR Update started broadcasting on the simulcast
        channel when this channel was not used for simulcasting (between 17 and 20 o'clock).
        The multiplex was operated by DIGI-TV I/S, a joint-venture between DR and TV 2.</p>
    <p class="lead">On June 11, 2007 the Danish Ministry of Culture announced that the six
        parties had agreed on how the extension of the network of a total of 8 multiplexes
        would continue. Four multiplexes (MUX 3-6) would be given to a commercial gatekeeper
        which would be required to have a varied offering and would be chosen through
        a "beauty contest". DIGI-TV, would be given another multiplex, MUX 2, in addition
        to its MUX 1. MUX 2 would be carrying the children and culture channel from DR,
        a parliament channel, more airtime for the TV 2 regions and community television.
        MUX7 was on June 29, 2009 allocated to use for mobile broadband, set to launch in 2013.
        The fate of MUX8 was not decided.</p>
    <p class="lead">On March 27, 2008 Boxer got the gatekeeper rights for MUX 3, 4 and 5.
        Boxer went on-air on 2 February 2009 with its three commercial multiplexes.
        The commercial gatekeeper would be required to carry at least one channel
        from neighbouring countries and a local channel. A maximum of 25 percent of the
        capacity could be used for "high-pay" channels (such as Canal+).</p>
    <p class="lead">The official launch was made on 1 November 2009,
        which also marked the launch of the new public channels DR Ramasjang,
        DR K, DR HD (replaced by DR3 in January 2013) and a 24‑hour version
        of DR Update (replaced by DR Ultra in March 2013). Also on this day,
        Copenhagen-based company Open Channel Aps launched a trial multiplex,
        "MUXCPH", with local channals such as Kanal København.</p>
